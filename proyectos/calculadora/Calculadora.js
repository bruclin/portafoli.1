class Calculadora {
    
    sumar(num1, num2) {

        return num1 + num2

    }

    restar(num1, num2) {

        return num1 - num2

    }

    multipicar(num1, num2) {

        return num1 * num2
        
    }

    dividir(num1, num2) {

        return num1 / num2
        
    }

    
}